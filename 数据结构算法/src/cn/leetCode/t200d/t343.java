    public int integerBreak(int n) {
        if (n == 2) {
            return 1;
        }
        int res = 0;
        for (int i = 1; i <= n - 1; i++) {
            res = Math.max(res,Math.max(i*(n-i),i*integerBreak(n-i)));
        }
        return res;
    }

